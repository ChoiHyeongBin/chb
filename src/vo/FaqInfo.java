package vo;

public class FaqInfo {
	private int fl_num, al_num;
	private String fl_kind, fl_title, fl_content;
	
	public int getFl_num() {
		return fl_num;
	}
	public void setFl_num(int fl_num) {
		this.fl_num = fl_num;
	}
	public int getAl_num() {
		return al_num;
	}
	public void setAl_num(int al_num) {
		this.al_num = al_num;
	}
	public String getFl_kind() {
		return fl_kind;
	}
	public void setFl_kind(String fl_kind) {
		this.fl_kind = fl_kind;
	}
	public String getFl_title() {
		return fl_title;
	}
	public void setFl_title(String fl_title) {
		this.fl_title = fl_title;
	}
	public String getFl_content() {
		return fl_content;
	}
	public void setFl_content(String fl_content) {
		this.fl_content = fl_content;
	}
}
