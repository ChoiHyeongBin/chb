package vo;

public class MemberCouponInfo {
	private int mc_num, ac_num, ac_discount;
	private String ml_id, ac_name, mc_use, mc_date, mc_usedate;
	
	public int getMc_num() {
		return mc_num;
	}
	public void setMc_num(int mc_num) {
		this.mc_num = mc_num;
	}
	public int getAc_num() {
		return ac_num;
	}
	public void setAc_num(int ac_num) {
		this.ac_num = ac_num;
	}
	public int getAc_discount() {
		return ac_discount;
	}
	public void setAc_discount(int ac_discount) {
		this.ac_discount = ac_discount;
	}
	public String getMl_id() {
		return ml_id;
	}
	public void setMl_id(String ml_id) {
		this.ml_id = ml_id;
	}
	public String getAc_name() {
		return ac_name;
	}
	public void setAc_name(String ac_name) {
		this.ac_name = ac_name;
	}
	public String getMc_use() {
		return mc_use;
	}
	public void setMc_use(String mc_use) {
		this.mc_use = mc_use;
	}
	public String getMc_date() {
		return mc_date;
	}
	public void setMc_date(String mc_date) {
		this.mc_date = mc_date;
	}
	public String getMc_usedate() {
		return mc_usedate;
	}
	public void setMc_usedate(String mc_usedate) {
		this.mc_usedate = mc_usedate;
	}
	
}
